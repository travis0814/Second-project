package main;

public class MyLabA {
	
	public int add(int a, int b) {
		return a+b;
	}
    public int subtract(int a, int b) {
    	return a-b;
    }

    public int multiply(int a, int b) {
    	return a*b;
    }
    
    public double divide(int a, int b) {
        return (double)a / (double)b;
    }

    public double circumference(double a) {
        return a*Math.PI;
    }




}






